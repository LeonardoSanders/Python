#Estrutura de dados - Set
#Set são dados mutáveis, porém, aceitam apenas tipos
#imutáveis como valor inteiro.

#Criando um set
#Set (iterável) ou {1, 2, 3}

s1 = set('Leonardo')
print(s1)

s2 = {'Leo', 1, 2, 3}
print(s2)

#OBS: O Set retorna uma sequência aleatória do valor contido nele. Ou seja, ele
#itera/iterage sobre os dados.
